    <script>
      function thankDonor() {
        const name = document.getElementById("donorName").value.trim();
        const email = document.getElementById("donorEmail").value.trim();
        const amount = document.getElementById("donationAmount").value.trim();

        if (!name || !email || !amount) {
          alert("If you're donating anonymously, just close this window. Otherwise, please fill in your details.");
          return;
        }

        fetch('donation-handler.php', {
          method: 'POST',
          headers: {
            'Content-Type': 'application/x-www-form-urlencoded',
          },
          body: new URLSearchParams({
            name,
            email,
            amount
          })
        })
        .then(res => res.json())
        .then(data => {
          if (data.success) {
            alert("Thank you for your donation! We've received your details.");
            $('#donationModal').modal('hide');
            document.querySelector("#donate form").reset();
          } else {
            alert("Failed: " + data.message);
          }
        })
        .catch(err => {
          alert("Error submitting donation info. Please try again.");
          console.error(err);
        });
      }
    </script>
